    /// @note This function shall be called after processing of FindService/StartFindService was completed
    /// @uptrace{SWS_CM_00004, SWS_CM_00131}
    explicit TMS_EnvMonitorProxy(HandleType& handle)
        : mHandle(handle)
        , mInterface(std::make_unique<para::com::ProxyInterface>(handle.GetInstanceSpecifier(), handle.GetServiceHandle()))
        , getHumidity(mInterface.get())
        , getLightIntensity(mInterface.get())
        , getTemp(mInterface.get())
        , notifyHumidity(mInterface.get())
        , notifyLightIntensity(mInterface.get())
        , notifyTemp(mInterface.get())
    {
    }
    /// @brief Destructor
    /// @uptrace{SWS_CM_10446}
    ~TMS_EnvMonitorProxy() = default;
    /// @brief Delete copy constructor
    /// @uptrace{SWS_CM_00136}
    TMS_EnvMonitorProxy(TMS_EnvMonitorProxy& other) = delete;
    /// @brief Delete copy assignment
    /// @uptrace{SWS_CM_00136}
    TMS_EnvMonitorProxy& operator=(const TMS_EnvMonitorProxy& other) = delete;
    /// @brief Move constructor
    /// @uptrace{SWS_CM_00137}
    TMS_EnvMonitorProxy(TMS_EnvMonitorProxy&& other) noexcept
        : mHandle(std::move(other.mHandle))
        , mInterface(std::move(other.mInterface))
        , getHumidity(std::move(other.getHumidity))
        , getLightIntensity(std::move(other.getLightIntensity))
        , getTemp(std::move(other.getTemp))
        , notifyHumidity(std::move(other.notifyHumidity))
        , notifyLightIntensity(std::move(other.notifyLightIntensity))
        , notifyTemp(std::move(other.notifyTemp))
    {
        mInterface->StopFindService();
        other.mInterface.reset();
    }
    /// @brief Move assignment
    /// @uptrace{SWS_CM_00137}
    TMS_EnvMonitorProxy& operator=(TMS_EnvMonitorProxy&& other) noexcept
    {
        mHandle = std::move(other.mHandle);
        mInterface = std::move(other.mInterface);
        mInterface->StopFindService();
        getHumidity = std::move(other.getHumidity);
        getLightIntensity = std::move(other.getLightIntensity);
        getTemp = std::move(other.getTemp);
        notifyHumidity = std::move(other.notifyHumidity);
        notifyLightIntensity = std::move(other.notifyLightIntensity);
        notifyTemp = std::move(other.notifyTemp);
        other.mInterface.reset();
        return *this;
    }
    /// @brief Stop the running StartFindService
    /// @uptrace{SWS_CM_00125}
    void StopFindService(ara::com::FindServiceHandle /*handle*/)
    {
        mInterface->StopFindService();
    }
    /// @brief Return handle that is connected with service provider currently
    /// @uptrace{SWS_CM_10383}
    HandleType GetHandle() const
    {
        return mHandle;
    }
    
private:
    HandleType mHandle;
    std::unique_ptr<para::com::ProxyInterface> mInterface;
    
public:
